//Write a program to print the short hand
class Short_Hand
  {
    public static void main(String args[])
    {
      int s=10;
      System.out.println(s);
      s+=2;
      System.out.println(s);
      s-=4;
      System.out.println(s);
      s*=2;
      System.out.println(s);
      s/=3;
      System.out.println(s);
      s%=6;
      System.out.println(s);
    }
  }