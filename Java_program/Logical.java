//Write a program to print logical and relational operators
class Logical
  {
    public static void main(String args[])
    {
      System.out.println((2>4) && (3==3));
      System.out.println((8<15) && (5<=8));
      System.out.println((12>=12) && (7<=4));
      System.out.println((2>3) || (12==12));
      System.out.println((18<24) || (12==12));
      System.out.println((3>9) || (17<=24));
      System.out.println((5>18)|| (22>42));
    }
  }