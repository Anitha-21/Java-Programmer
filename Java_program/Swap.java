//Write the program to print the swap with third variable
class Swap
  {
    public static void main(String args[]) {
      int a,b,c;
      a=10;
      b=15;
      c=0;
      //Before swapping
      c=a;
      a=b;
      b=c;
      System.out.println("the swap of a is "+a);
      System.out.println("the swap of b is "+b);
    }
  }