 //write a program of assignment operator
class Assignment
  {
    public static void main(String args[])
    {
      int s=3;
      System.out.println(s);
      s=s+5;
      System.out.println(s);
      s=s-2;
      System.out.println(s);
      s=s/6;
      System.out.println(s);
      s=s*4;
      System.out.println(s);
      s=s%3;
      System.out.println(s);
    }
  }