//Write a program on shift operators
class Shift{
  public static void main(String args[])
  {
    System.out.println("Shift right (>>) " +(6>>2));
    System.out.println("Shift left (<<) " +(8<<2));
    System.out.println("Shift right (>>) " +(20>>4));
    System.out.println("Shift left (<<) " +(40<<4));
    System.out.println("Shift right (>>) " +(120>>8));
    System.out.println("Shift left (<<) " +(400<<8));
  }
}